	private static RegistryObject makeVerticalVanillaSlab(String name, Block slabBlock) {
		String fullName = String.format("%s_vertical_slab", name);
		return BLOCKS.register(fullName, () -> new VerticalOnlySlabBlock(slabBlock));
	}

